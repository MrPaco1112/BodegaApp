    public ConsultaGeneral() {
        initComponents();
        try {
        ArrayList<Elemento> elementos = AsistenteFirebase.obtenerTodosLosElementos(manager.firebaseDatabase, "Bodega"); // Obtiene los elementos
        StringBuilder texto = new StringBuilder();
        
        if (elementos.isEmpty()) {
            texto.append("No hay elementos en la base de datos.");
        } else {
             for (int i = 0; i < elementos.size(); i++) {
                Elemento elemento = elementos.get(i);
                texto.append("ID: ").append(i)
                     .append(" | Nombre: ").append(elemento.getNombre())
                     .append(" | Precio: ").append(elemento.getPrecio())
                     .append("\n");
            }
        }
        
        jTextArea1.setText(texto.toString()); // Mostrar en el JTextArea
        
        } catch (InterruptedException e) {
            jTextArea1.setText("Error al recuperar datos: " + e.getMessage());
            e.printStackTrace();
        }
    }

 
