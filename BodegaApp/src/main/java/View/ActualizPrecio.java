    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String input2 = JOptionPane.showInputDialog("Ingresa el precio nuevo:");
        jLabel2.setText(input2);
        try{
        Double numero = Double.valueOf(input2.trim());
        elemento.setPrecio(numero);
       } catch (NumberFormatException e) {
            System.out.println("Error: el valor no es un número válido.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Es la informacion a actualizar correcta?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                AsistenteFirebase.actualizarInfo(manager.firebaseDatabase, "Bodega", clave, elemento);
                this.dispose(); // Cierra esta ventana
                new MenuPrincipal().setVisible(true);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

